package com.zengshi.butterfly.core.afs;

public class AFSException extends Exception {

	public AFSException() {
		super();
	}

	public AFSException(String message, Throwable cause) {
		super(message, cause);
	}

	public AFSException(String message) {
		super(message);
	}

	public AFSException(Throwable cause) {
		super(cause);
	}

}
